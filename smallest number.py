# Basic calculations
# Calculator program in python using input() and format() functions

# Promting input from the user

n1 = float(input("Enter first number"))
n2 = float(input("Enter second number"))

# addition
print("{} + {} = ".format(n1, n2))
print(n1 + n2)

# subtraction
print("{} - {} = ".format(n1, n2))
print(n1 - n2)


